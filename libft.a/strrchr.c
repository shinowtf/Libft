#include "libft.h"

char	*strrchr(const char *s, int c)
{
	int n;

	n = 0;
	while(s[n] != '\0')
		n++;
	s = s + n - 1;
	 while (*s != NULL)
	 {
		 if (*s == c)
			return ((char *) s);
		s--;
	 }
	return(NULL);
}
