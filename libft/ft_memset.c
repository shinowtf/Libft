#include "libft.h"

void	*ft_memset(void *ptr, int c, size_t n)
{
	char	*s;
	int		i;

	i = 0;
	s = ptr;
	while (n-- > 0)
	{
		s[i] = c;
		i++;
	}
	return (ptr);
}
